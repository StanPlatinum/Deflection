static void sort(unsigned int left, unsigned int right)
{
    unsigned int i, j;
    unsigned long pivot, tmp;
    if ((int)left >= (int)right) return; // potentially under-flow!
    pivot = numbers[left];
    i = left;
    for (j = left + 1;j <= right; ++j) {
        if (numbers[j] < pivot) {
            ++i;
            tmp = numbers[j]; numbers[j] = numbers[i]; numbers[i] = tmp;
        }
    }
    tmp = numbers[left]; numbers[left] = numbers[i]; numbers[i] = tmp;
    sort(left, i-1);
    sort(i+1, right);
}

static int check()
{
    unsigned int i;
    for (i = 1;i < array_size;++i) {
        if (numbers[i] < numbers[i-1])
            return 0;
    }
    return 1;
}

static unsigned int binsearch(uint32_t k)
{
    unsigned int b, e, m;
    b = 0;
    e = array_size-1;
    while(b < e) {
        uint32_t o;
        m = (b+e+1) / 2;
        o = numbers[m];
        if (k == o)
            return m;
        else if (k < o)
            e = m-1;
        else
            b = m;
    }
    return b;
}

